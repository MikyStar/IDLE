import React from 'react';

//////////////////////////////////////////////////////////////////////
interface Props
{
	name ?: string,
	file : string,
	ratio ?: number,
	style ?: React.CSSProperties
}

//////////////////////////////////////////////////////////////////////

const DEFAULT_RATIO = '30px';

//////////////////////////////////////////////////////////////////////

const Icon : React.FC<Props> = ( { name, file, style, ratio } : Props ) =>
	<img
		src={ file }
		alt={ name }
		title={ name }
		style={{ ...style, height : ratio || DEFAULT_RATIO, width : ratio || DEFAULT_RATIO }}
	/>

//////////////////////////////////////////////////////////////////////

export default Icon;